# Hacer una clase estática que pueda ser llamada y devuelva unas operaciones
# matemáticas con los números 40 y 10.

class Library:
    @staticmethod
    def mathOperations():
        print("40 + 10 =", 40 + 10)
        print("40 - 10 =", 40 - 10)
        print("40 * 10 =", 40 * 10)
        print("40 / 10 =", 40 / 10)
        print("40 % 10 =", 40 % 10)
        print("40 // 10 =", 40 // 10)
        print("40 ** 10 =", 40 ** 10)
        print("40 ^ 10 =", 40 ^ 10)
        print("40 & 10 =", 40 & 10)
        print("40 | 10 =", 40 | 10)
        print("40 << 10 =", 40 << 10)
        print("40 >> 10 =", 40 >> 10)
        print("40 == 10 =", 40 == 10)
        print("40 != 10 =", 40 != 10)
        print("40 > 10 =", 40 > 10)
        print("40 < 10 =", 40 < 10)
        print("40 >= 10 =", 40 >= 10)
        print("40 <= 10 =", 40 <= 10)
        print("40 and 10 =", 40 and 10)
        print("40 or 10 =", 40 or 10)
        print("not 40 =", not 40)
        print("not 10 =", not 10)
